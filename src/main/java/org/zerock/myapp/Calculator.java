package org.zerock.myapp;



public class Calculator {    
	
	// 덧셈 메서드
    public int 더하기(int a, int b) {
        return a + b;
    }

    // 뺄셈 메서드
    public int 빼기(int a, int b) {
        return a - b;
    }

    // 곱셈 메서드
    public int 곱하기(int a, int b) {
        return a * b;
    }

    // 나눗셈 메서드
    public int 나누기(int a, int b) {
        return a / b;
    }

}
